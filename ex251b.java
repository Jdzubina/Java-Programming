public class ex251b {
//assignment operators
    public static void main (String [] args){

        int x = 15;
        int y = 7;

        x += y;
        System.out.println(x);

        x *= y;
        System.out.println(x);

        x -= y;
        System.out.println(x);

        x /= y;
        System.out.println(x);

    }
}
